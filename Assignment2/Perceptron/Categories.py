class category:
    def __init__(self, catName, poisonCount, totalCount):
        self.poisonCount = poisonCount
        self.totalCount = totalCount
        self.catName = catName

    def setName(name):
        catName = name

    def increasePoison():
        poisonCount = poisonCount + 1

    def increaseTotal():
        totalCount = totalCount + 1

